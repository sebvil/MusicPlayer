@Composable
fun AppTheme(
    colors: Colors = if (isSystemInDarkTheme()) DarkM2ThemeColors else LightM2ThemeColors,
    typography: Typography = Typography(defaultFontFamily = Roboto),
    shapes: Shapes = androidx.compose.material.MaterialTheme.shapes,
    content: @Composable () -> Unit
) {
    androidx.compose.material.MaterialTheme(
        colors = colors,
        typography = typography,
        shapes = shapes,
        content = content
    )
}

@Composable
fun M3AppTheme(
    useDarkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colors = if (!useDarkTheme) {
        LightThemeColors
    } else {
        DarkThemeColors
    }

    MaterialTheme(
        colorScheme = colors,
        typography = AppTypography,
        content = content
    )
}